import pandas as pd
import numpy as np
from sklearn.ensemble import RandomForestClassifier

# Load attendance dataset
attendance = pd.read_csv("/content/internship_attendance_40days.csv")

# Calculate attendance statistics
summary = attendance.groupby("Student_Name")["Present"].sum().reset_index()
summary.rename(columns={"Present": "Days_Attended"}, inplace=True)

TOTAL_DAYS = 40

summary["Attendance_Percentage"] = (
    summary["Days_Attended"] / TOTAL_DAYS
) * 100

summary["Certificate"] = np.where(
    summary["Attendance_Percentage"] >= 80,
    1,
    0
)

# ML Model
X = summary[["Days_Attended", "Attendance_Percentage"]]
y = summary["Certificate"]

model = RandomForestClassifier(
    n_estimators=100,
    random_state=42
)

model.fit(X, y)

# Final Statistics
total_students = len(summary)
eligible_students = int(summary["Certificate"].sum())
not_eligible_students = total_students - eligible_students

print("Total Students:", total_students)
print("Students Eligible for Certificate:", eligible_students)
print("Students Not Eligible for Certificate:", not_eligible_students)

# Save result
summary.to_csv(
    "attendance_summary.csv",
    index=False
)